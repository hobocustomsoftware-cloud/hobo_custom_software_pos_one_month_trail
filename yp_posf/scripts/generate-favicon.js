/**
 * Generate public/logo.png (minimal 67-byte PNG) so favicon /logo.png exists.
 * Run: node scripts/generate-favicon.js
 * No npm deps. When logo.svg is the main asset, consider replacing this with SVG→PNG if needed.
 */
import { writeFileSync, existsSync } from 'fs'
import { fileURLToPath } from 'url'
import { dirname, join } from 'path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const pngPath = join(__dirname, '..', 'public', 'logo.png')

// Smallest valid PNG (1x1 transparent pixel), 67 bytes
const minimalPng = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1f, 0x15, 0xc4, 0x89, 0x00, 0x00, 0x00,
  0x0a, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9c, 0x63, 0x00, 0x01, 0x00, 0x00,
  0x05, 0x00, 0x01, 0x0d, 0x0a, 0x2d, 0xb4, 0x00, 0x00, 0x00, 0x00, 0x49,
  0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82
])

writeFileSync(pngPath, minimalPng)
console.log('Created public/logo.png (minimal placeholder)')
