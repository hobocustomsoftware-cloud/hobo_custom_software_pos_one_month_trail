export function useI18n() {
  const localeStore = useLocaleStore()
  function t(key) {
    const loc = localeStore.locale === 'en' ? 'en' : 'my'
    return strings[loc][key] ?? key
  }
  return { t, locale: computed(() => localeStore.locale), setLocale: localeStore.setLocale, setLang: localeStore.setLang }
}
